package java_240220.com.library.management;

public class Book {
    // 필드
    private String title;
    private String author;

    // 생성자
    Book() {
        this.title = title;
        this.author = author;

    }

    // 메서드
    // Getter 메서드

    public String getAuthor() {
        return author;
    }

    public String getTitle() {
        return title;
    }


}

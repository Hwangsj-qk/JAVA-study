package JAVA_240214.array;

public class Array1 {
    public static void main(String[] args) {
        int score1 = 80;
        int score2 = 70;
        int score3 = 90;
        int score4 = 50;
        int score5 = 75;
        int score6 = 60;

        System.out.println("학생 1번 점수: " + score1);
        System.out.println("학생 2번 점수: " + score2);
        System.out.println("학생 3번 점수: " + score3);
        System.out.println("학생 4번 점수: " + score4);
        System.out.println("학생 5번 점수: " + score5);
        System.out.println("학생 6번 점수: " + score6);

        // 학생 수가 증가하면서 코딩의 양이 비례하여 증가 (불편) -> 반복문도 사용 안됨
    }
}

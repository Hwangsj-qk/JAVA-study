package JAVA_240207.variable;

public class Type1 {
    public static void main(String[] args) {
        int a = 100; // 정수
        double b = 20.5; // 실수(정수를 제외한 수)
        boolean c = true;  // 불리언(true or false)
        char d = 'A';   // 캐릭터: '문자 하나'를 작은 따옴표 안에 사용
        String e = "안녕하세요"; // 문자열: 큰 따옴표 안에 사용

    }
}
